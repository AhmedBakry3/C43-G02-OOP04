namespace Assignment_Session_4.Classes
{
    internal class Calculator
    {
        #region Methods
        //Add Method to Accept two integers and return their sum.
        public int Add(int A, int B)
        {
            return A + B;
        }

        //Add Method to Accept three integers and return their sum.
        public int Add(int A, int B, int C)
        {
            return A + B + C;
        }

        //Add Method to Accept two doubles and return their sum.
        public double Add(double A, double B)
        {
            return A + B;
        }
        #endregion
    }
}
